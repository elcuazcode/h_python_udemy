
## Recorer una lista con for
## funciona igual para una tupla
nombres = ["juan", "pedro", "chilo"]

for i in nombres:
  print(f"Y el nombre ganador es: {i}")

# Recorer un diccionario con for
nacionalidad = {"pepe":"guate", "carlos":"nica", "luis":"checo"}

for i in nacionalidad:
  print(f"Su nombres es {i} su nacionalidad es {nacionalidad[i]}")
